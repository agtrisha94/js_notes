//Prints value of PI
console.log(Math.PI);

//Returns integer part by removing any fractional digits
console.log(Math.trunc(3.7));

//Rounds up no.
console.log(Math.round(4.2));

//Rounds up to nearest integer(largest)
console.log(Math.ceil(5.8));

//Rounds up to nearest integer(smallest)
console.log(Math.floor(-4.8));

//Math.pow(base,exponent)
console.log(Math.pow(2,3));

//Returns minimum no.
console.log(Math.min(2,5,3));

//Returns max no.
console.log(Math.max(3,6,2));

//Returns random no. between 0 and 1 (Including 0 and excluding 1)
console.log(Math.random());

//Random no. between 0 and 1
console.log((Math.random()*10)+1);